from math import *

if __name__ == "__main__":
    print(abs(-1))  #
    print(2 ** 3)  # N次方
    print(pow(2, 3))  # N次方
    print(7 / 3)
    print(round(float("1.8888"), 3))  # 四舍五入
    print(floor(7.52))  # the greatest Integral <= x
